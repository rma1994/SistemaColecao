    //Adiciona o Internal Frame Usuario ao desktopPane
    public void addCadUsuarioNew(JDesktopPane frame){
        this.jDesktopPanePrincipal = frame;
        
        //Esconde e desabilita o botao EXCLUIR
        jButtonExluir.setVisible(false);
        jButtonExluir.setEnabled(false);
        
        //Adiciona a data e o codigo no documento
        addData();
        addCod();
                
        //Adiciona o Internal Frame Usuario ao desktopPane
        frame.add(this);
        this.setVisible(true);
    }
    
    //coloca a data atual no campo de data
    private void addData(){
        Date data = new Date();
        jTextDCadastro.setText(sdf1.format(data).toString());
    }
    
    //pega o ultimo codigo e soma 1 a ele, assim gerando o novo id a ser usado.
    private void addCod(){
        int cod;
        cod = usuariodao.getMaxCod();
        cod++;
        
        jTextCod.setText(Integer.toString(cod));
    }
    
    //Valida se os dados de clientes estao preenchidos.
    private boolean validaUsuario(Usuario usuario){
        boolean valido = false;
        
        //se os campos forem preenchidos, torna o formulario valido
        if((usuario.getNome() != null) || (usuario.getSenha() != null)){
            valido = true;
            System.out.println("validado");
        }
        
        return valido;
    }
    
    //Seta os documents dos campos de texto.
    private void setDocuments(){
        jTextNome.setDocument(new ControleTexto(60));
        jPassword.setDocument(new ControleTexto(8));
    }
    
