    //Adiciona o Internal Frame livro ao desktopPane para o cadastro de um novo livro
    public void addCadLivroNew(JDesktopPane frame){
        this.jDesktopPanePrincipal = frame;
        
        //Diz que esse livro nao é para update e sim para insert
        this.livro.setUpdate(false);
        
        //Esconde e desabilita o botao EXCLUIR
        jButtonExluir.setVisible(false);
        jButtonExluir.setEnabled(false);
        
        //Adiciona a data e o codigo no documento
        addData();
        addCod();
                
        //Adiciona o Internal Frame livro ao desktopPane
        frame.add(this);
        this.setVisible(true);
    }
    
    //Adiciona o Internal Frame livro ao desktopPane para a atualização de um usuario ja existente
    public void addCadLivroUpdate(JDesktopPane frame, Livro livro){
        this.livro = livro;
        
        
        //Pega os valor do livro em questao e os adiciona aos seus respectivos campos
        jTextCod.setText(Integer.toString(livro.getCod()));
        jTextNome.setText(livro.getNome());
        jTextPaginas.setText(livro.getPaginas());
        jTextDCadastro.setText(sdf1.format(livro.getData()).toString());
        
        //Adiciona o Internal Frame livro ao desktopPane
        frame.add(this);
        this.setVisible(true);
    }
    
    //coloca a data atual no campo de data
    private void addData(){
        Date data = new Date();
        jTextDCadastro.setText(sdf1.format(data).toString());
    }
    
    //pega o ultimo codigo e soma 1 a ele, assim gerando o novo id a ser usado.
    private void addCod(){
        int cod;
        cod = livrodao.getMaxCod();
        cod++;
        
        jTextCod.setText(Integer.toString(cod));
    }
    
    //Valida se os dados de livro estao preenchidos.
    private boolean validaUsuario(Livro livro){
        boolean valido = false;
        
        //se os campos forem preenchidos, torna o formulario valido
        if((livro.getNome() != null) && (livro.getPaginas()!= null) && (!livro.getNome().equals("")) && (!livro.getPaginas().equals(""))){
            valido = true;
            System.out.println("validado");
        }
        
        return valido;
    }
    
    //Seta os documents dos campos de texto.
    private void setDocuments(){
        jTextNome.setDocument(new ControleTexto(60));
        jTextPaginas.setDocument(new ControleTexto(6));
    }
    
    
