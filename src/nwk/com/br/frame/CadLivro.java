    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        Livro livro = new Livro();
        boolean valido = false;
        boolean inserido = false;
        
        livro.setCod(Integer.parseInt(jTextCod.getText()));
        livro.setNome(jTextNome.getText());
        livro.setPaginas(jTextPaginas.getText());
        livro.setData(jTextDCadastro.getText());
        
        
        valido = validaUsuario(livro);
        
        if(valido == true){
            if(this.livro.isUpdate() == false){
                
                //Recebe o retorno se o usuario foi inserido com sucesso ou nao
                inserido = livrodao.inserir(livro);
                if(inserido == true){
                    JOptionPane.showMessageDialog(null, "Livro Inserido Com Sucesso");
                    dispose();
                } 
                
            }else if(this.livro.isUpdate() == true){
                //Recebe o retorno se o usuario foi atualizado com sucesso ou nao
                inserido = livrodao.atualizar(livro);
                if(inserido == true){
                    JOptionPane.showMessageDialog(null, "Livro Atualizado Com Sucesso");
                    dispose();
                } 
            }
            
        } else if(valido == false){
            JOptionPane.showMessageDialog(null, "Campos Não preenchidos, verifique!");
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

